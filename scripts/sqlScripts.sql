CREATE TABLE DRUGS 
(
ID NUMBER(19,0) NOT NULL , 
NAME VARCHAR2(255), 
PRICE NUMBER(38,0), 
VERSION NUMBER(10,0), 
PRIMARY KEY (ID)
);


CREATE TABLE PATIENTS 
(
ID NUMBER(19,0) NOT NULL , 
DOB DATE, 
EMAIL VARCHAR2(255), 
GENDER VARCHAR2(255), 
PASSWORD VARCHAR2(255), 
PHONE VARCHAR2(255), 
SSN VARCHAR2(255), 
STATUS VARCHAR2(255), 
USERNAME VARCHAR2(255), 
VERSION NUMBER(10,0), 
CITY VARCHAR2(255), 
COUNTRY VARCHAR2(255),  
STATE VARCHAR2(255), 
STREET1 VARCHAR2(255), 
STREET2 VARCHAR2(255), 
ZIP VARCHAR2(255), 
FIRSTNAME VARCHAR2(255), 
LASTNAME VARCHAR2(255), 
MIDDLENAME VARCHAR2(255), 
PRIMARY KEY (ID), 
UNIQUE (EMAIL), 
UNIQUE (SSN), 
UNIQUE (USERNAME)
);



CREATE TABLE PHYSICIANS 
(	
ID NUMBER(19,0) NOT NULL , 
EMAIL VARCHAR2(255 ), 
PASSWORD VARCHAR2(255), 
PHONE VARCHAR2(255 ), 
USERNAME VARCHAR2(255), 
VERSION NUMBER(10,0), 
FIRSTNAME VARCHAR2(255), 
LASTNAME VARCHAR2(255), 
MIDDLENAME VARCHAR2(255), 
PRIMARY KEY (ID), 
UNIQUE (EMAIL), 
UNIQUE (USERNAME)
);


CREATE TABLE RECORDS 
(
ID NUMBER(19,0) NOT NULL , 
CREATE_DATE DATE, 
DIAGNOSIS VARCHAR2(255 ), 
NOTES VARCHAR2(255 ), 
SYMPTOMS VARCHAR2(255 ), 
VERSION NUMBER(10,0), 
DIASTOLIC_BLOOD_PRESSURE NUMBER(19,4), 
HEIGHT NUMBER(19,4), 
PULSE NUMBER(19,4), 
SYSTOLIC_BLOOD_PRESSURE NUMBER(19,4), 
TEMPERATURE NUMBER(19,4), 
WEIGHT NUMBER(19,4), 
PATIENT_ID NUMBER(19,0) NOT NULL , 
PHYSICIAN_ID NUMBER(19,0) NOT NULL , 
PRIMARY KEY (ID), 
CONSTRAINT FK_RECORDS_PHYSICIAN_ID FOREIGN KEY (PHYSICIAN_ID) REFERENCES PHYSICIANS (ID) , 
CONSTRAINT FK_RECORDS_PATIENT_ID FOREIGN KEY (PATIENT_ID)
REFERENCES PATIENTS (ID) 
);

CREATE TABLE PATIENTS_RECORDS 
(
PATIENT_ID NUMBER(19,0) NOT NULL , 
RECORDS_ID NUMBER(19,0) NOT NULL , 
PRIMARY KEY (PATIENT_ID, RECORDS_ID), 
CONSTRAINT FK_PATIENTS_RECORDS_RECORDS_ID FOREIGN KEY (RECORDS_ID) REFERENCES RECORDS (ID) , 
CONSTRAINT FK_PATIENTS_RECORDS_PATIENT_ID FOREIGN KEY (PATIENT_ID)
REFERENCES PATIENTS (ID) 
);


CREATE TABLE PRESCRIPTIONS 
(	
ID NUMBER(19,0) NOT NULL , 
DATE_PRESCRIBED DATE, 
DOSAGE NUMBER(10,0), 
FREQUENCY VARCHAR2(255 ), 
INSTRUCTIONS VARCHAR2(255 ), 
REFILLS_REMAINING NUMBER(10,0), 
VERSION NUMBER(10,0), 
DRUG_ID NUMBER(19,0) NOT NULL , 
PRIMARY KEY (ID), 
CONSTRAINT FK_PRESCRIPTIONS_DRUG_ID FOREIGN KEY (DRUG_ID) REFERENCES DRUGS (ID) 
);


CREATE TABLE RECORDS_PRESCRIPTIONS 
(	
RECORD_ID NUMBER(19,0) NOT NULL , 
PRESCRIPTIONS_ID NUMBER(19,0) NOT NULL , 
PRIMARY KEY (RECORD_ID, PRESCRIPTIONS_ID), 
CONSTRAINT RCRDSPRSCRIPTIONSPRSCRPTIONSID FOREIGN KEY (PRESCRIPTIONS_ID) REFERENCES PRESCRIPTIONS (ID) , 
CONSTRAINT RECORDS_PRESCRIPTIONSRECORD_ID FOREIGN KEY (RECORD_ID) REFERENCES RECORDS (ID) 
);



CREATE TABLE SEQUENCE 
(	
SEQ_NAME VARCHAR2(50) NOT NULL, 
SEQ_COUNT NUMBER(38,0), 
PRIMARY KEY (SEQ_NAME)
);


